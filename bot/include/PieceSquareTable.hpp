constexpr int GetStaticPositionalPieceValue(Representation::Pieces::Piece piece, int squareIndex, float phase) {
    if(piece == Representation::Pieces::Kind::NONE) return 0;
    auto isWhite= Representation::Pieces::IsColor(piece, Representation::Pieces::Color::WHITE);
    int trueSquareIndex= isWhite ? squareIndex : Representation::Coordinates::Flip(squareIndex);
    auto pieceKind= Representation::Pieces::GetKind(piece);
    int mgValue= MiddleGamePieceSquareTable[pieceKind][trueSquareIndex];
    int egValue= EndGamePieceSquareTable[pieceKind][trueSquareIndex];

    return static_cast<int>(mgValue * phase + egValue * (1.0f - phase));
}
